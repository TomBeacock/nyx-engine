Nyx::KeyCode translate_key_code(USHORT key_code)
{
    assert(key_code <= 0x00FF);
    return key_code_table[key_code];
}

Nyx::ScanCode translate_scan_code(USHORT scan_code)
{
    assert(scan_code <= 0xE07f);
    if (scan_code > 0x7F) {
        scan_code = (scan_code & 0x0FFF) + 0x80;
    }
    return scan_code_table[static_cast<uint8_t>(scan_code)];
}

Nyx::MouseButton translate_mouse_button(USHORT mouse_button)
{
    switch (mouse_button) {
        case 0x0001:
        case 0x0002: return Nyx::MouseButton::Left;
        case 0x0004:
        case 0x0008: return Nyx::MouseButton::Right;
        case 0x0010:
        case 0x0020: return Nyx::MouseButton::Middle;
        case 0x0040:
        case 0x0080: return Nyx::MouseButton::X1;
        case 0x0100:
        case 0x0200: return Nyx::MouseButton::X1;
        default: return Nyx::MouseButton::None;
    }
}
